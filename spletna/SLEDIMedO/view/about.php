					</p>
					<br>
					<p>
					Pri tem smo se osredotočili na lokalne medije oziroma spletne časopise, kot tudi na socialne medije in projekte same po sebi. S pomočjo razlicnih računalniških tehnologij smo tako črpali relavantne novice o Evropskih projektih v Sloveniji. Rezultat projekta je celovita IKT rešitev, ki omogoča sledenje medijski pokritosti aktivnosti projektov makroregij, v katerih sodelujejo slovenski projektni partnerji. Programsko orodje omogoča hitro in učinkovito iskanje, pregledovanje, filtriranje in obdelavo medijskih objav izbranih slovenskih virov.
