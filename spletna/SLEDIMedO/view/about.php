					Center za evropsko prihodnost je ustanovila Vlada Republike Slovenije z namenom izboljšanje evropske prihodnosti Zahodnega Balkana, večje demokratičnosti, varnosti ter osredotočanje na mlajšo generacijo. Za ta namen organizirajo različne treninge in delavnice, konference in okrogle mize. Prav tako sodelujejo v različnih projektih, financiranih iz različnih virov. Trudijo se za regionalno stabilnost in blaginjo preko gradenj novih poznanstev ter s krepitvijo že obstoječih vez z drugimi državami, da bi dosegli večjo prepoznavnost slovenskih regij. CEP je do sedaj izvedel že preko 200 projektov ter sodeloval s preko 1800 strokovnjaki. Glavne karakteristike njihovih aktivnosti pa so multidisciplinarnost in interaktivnost. Tako se s pomočjo strokovnjakov, ki so vključeni že pri snovanju projekta, spopadajo s različnimi temami, h katerim pristopajo z večih vidikov, velikokrat tudi preko pripravnikov, ki pa tako dobijo dodatne spretnosti in znanja.
