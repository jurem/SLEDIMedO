					<p>
					Projekt 'Sledenje medijskim objavam' je financiran s strani Centra za evropsko prihodnosti (CEP). Cilj Centra za evropsko prihodnost je izboljšanje evropske prihodnosti, vecja demokraticnost, varnost ter osredotocanje na mlajšo generacijo.
					</p>
					<br>
					<p>
					Za ta namen organizirajo razne treninge in delavnice, konference in okrogle mize. Prav tako financirajo različne projekte pri katerih tudi sami sodelujejo.
