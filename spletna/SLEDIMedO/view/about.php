					Prav zaradi tega je včasih težko ali pa vsaj zahtevnejše najti relavantne informacije za določena iskanja, kar nam lahko v končni fazi vzame veliko časa. Večina projektov sicer ima svojo spletno stran in pogosto tudi profile na socialnih omrežjih, preko katerih zainteresirane obveščajo o svojih dejavnostih. A nemalokrat se tu prenos informacij ustavi, s čimer pomembno izgubijo vsi vpleteni - tako izvajalci projektov, ker se o njihovem delu premalo poroča, kot potencialni koristniki rezultatov projekta, ki ne dobijo informacij o možnostih, ki jih izvedeni projekti odpirajo. 
					</p>
