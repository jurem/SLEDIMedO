					CEP je do sedaj izvedel že preko 200 projektov ter sodeloval s preko 1800 strokovnjaki. Glavne karakteristike njihovih aktivnosti pa so multidisciplinarnost in interaktivnost. Tako se s pomočjo strokovnjakov, ki so vkljuceni že pri snovanju projekta, spopadajo s različnimi temami, h katerim pristopajo z večih vidikov, velikokrat tudi preko pripravnikov, ki pa tako dobijo dodatne spretnosti in znanja.
					</p>
