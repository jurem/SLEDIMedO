					<p>
					Glavni namen našega projekta je bilo preučiti možnosti ugotavljanja sledljivosti medijskim objavam s podrocja izvajanja EU projektov. Pri tem gre za uporabo inovativnih tehnologij, ki bi omogočale semi-avtomatsko analizo sledljivosti, preko kreativnega graficnega uporabniškega vmesnika. V okviru Študentskih inovativnih projektov za družbeno korist (ŠIPK) se tako izvaja projekt 'Sledenje medijskih objav' čigar namen je prav sledenje objavam ter hranjenje potencialnih objaviteljev novic o finančno podprtih projektih s strani Evropske unije (natancneje Danube Transnational Programme) in nenazadnje tudi formiranje spletne strani, ki bi omogočala iskanje teh projektov, oziroma novic o le-teh, na enostavnejši in prijetnejši nacin.
					</p>
					<br>
					<p>
					Pri tem smo se osredotočili na lokalne medije oziroma spletne časopise, kot tudi na socialne medije in projekte same po sebi. S pomočjo razlicnih računalniških tehnologij smo tako črpali relavantne novice o Evropskih projektih v Sloveniji.
					Rezultat projekta je celovita IKT rešitev, ki omogoča sledenje medijski pokritosti aktivnosti projektov makroregij, v katerih sodelujejo slovenski projektni partnerji. Programsko orodje omogoča hitro in učinkovito iskanje, pregledovanje, filtriranje in obdelavo medijskih objav izbranih slovenskih virov.
					</p>
